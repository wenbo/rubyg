ary = ["initial", "value"]      # => ["initial", "value"]
# 最初から5個が埋める対象だが、配列の長さは2なので拡張される。
ary.fill(0,5){|i| i}            # => [0, 1, 2, 3, 4]
# 全要素を埋める。
ary.fill {"A"}                  # => ["A", "A", "A", "A", "A"]
# インデックス0〜1を埋める。
ary.fill(0..1){"B"}             # => ["B", "B", "A", "A", "A"]
# インデックス3以降全てを埋める。
ary.fill(3) {"C"}               # => ["B", "B", "A", "C", "C"]
# 破壊的メソッドなのでオブジェクトが書き変わる。
ary                             # => ["B", "B", "A", "C", "C"]
